function list_Tel (){
    var Tel = document.getElementsByClassName("Telefony");
    var Slu = document.getElementsByClassName("Sluchawki");
    var Mys = document.getElementsByClassName("Myszki");
    var Klaw = document.getElementsByClassName("Klawiatury");
    var Pra = document.getElementsByClassName("Pralki");
    var Mik = document.getElementsByClassName("Miksery");
    var Tele = document.getElementsByClassName("Telewizory");
    var Mon = document.getElementsByClassName("Monitory");

    Array.from(Tel).forEach((x) => {
        x.style.display = "block";
    });

    Array.from(Slu).forEach((x) => {
        x.style.display = "none";
    });

    Array.from(Mys).forEach((x) => {
        x.style.display = "none";
    });

    Array.from(Klaw).forEach((x) => {
        x.style.display = "none";
    });

    Array.from(Pra).forEach((x) => {
        x.style.display = "none";
    });

    Array.from(Mik).forEach((x) => {
        x.style.display = "none";
    });

    Array.from(Tele).forEach((x) => {
        x.style.display = "none";
    });

    Array.from(Mon).forEach((x) => {
        x.style.display = "none";
    });
}

